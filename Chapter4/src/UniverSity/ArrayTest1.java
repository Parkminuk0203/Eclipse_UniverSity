package UniverSity;

public class ArrayTest1 {

	public static void main(String[] args) {
//		크기가 10인 정수형 배열을 생성하고 여기에 0부터 9까지의 값으로 배열을 채우는 프로그램을 살펴보자
		int[] arr = new int[10];
		
		// 값 채우기
		for(int i=0; i<arr.length; i++) {
			arr[i] += i;
		}
		
		// 출력
		for(int i=0; i<arr.length; i++) {
			System.out.print(arr[i]+" ");
		}

	} // end of main

} // end of class
